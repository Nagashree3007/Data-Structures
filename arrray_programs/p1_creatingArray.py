"""
    Description: Function to iterates the elements present in the Array and prints based on indexs
    Parameter: list of elements
    Return:elements and their index
    """
    

array_of_integers = [10, 20, 30, 40, 50]
print("Elements of the array based on index:")
for i in range(len(array_of_integers)):
   print(f"Element at index {i} = {array_of_integers[i]}")







